bool MooseAsks(string question)
{
    Console.Write($"{question} (Y/N): ");
    string answer = Console.ReadLine().ToLower();

    while (answer != "y" && answer != "n")
    {
        Console.Write($"{question} (Y/N): ");
        answer = Console.ReadLine().ToLower();
    }

    if (answer == "y")
    {
        return true;
    }
    else
    {
        return false;
    }
}

void AskTheMooseQuestions(string question, string answerYes, string answerNo )
{
    bool isTrue = MooseAsks($"{question}");
    if (isTrue)
    {
        MooseSays($"{answerYes}");
    }
    else
    {
        MooseSays($"{answerNo}");
    }
}
void AskPutinQuestions(string question, string answerYes, string answerNo )
